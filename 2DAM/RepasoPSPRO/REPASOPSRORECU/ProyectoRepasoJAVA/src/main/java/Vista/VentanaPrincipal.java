    public void cargarTablaAlumnos(){
        try{
            int fila = tablaColegio.getSelectedRow();
            int codigoColegio = Integer.parseInt(tablaColegio.getValueAt(fila, 0).toString());
            ResultSet resultset = daoAlumno.getAlumnos(codigoColegio);

        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Código Colegio");
        modelo.addColumn("Nombre");
        modelo.addColumn("Edad");
        modelo.addColumn("Feo");
        
        Object[] registroLeido = new Object[4];
        while(resultset.next()) {
            registroLeido[0] = resultset.getInt("idColegio");
            registroLeido[1] = resultset.getString("nombre");
            registroLeido[2] = resultset.getInt("edad");
            registroLeido[3] = resultset.getBoolean("feo");
                        
            modelo.addRow(registroLeido);
        }
        tablaAlumnos.setModel(modelo);
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }
    
     public void cargarTablaColegio() {
         try{
             ResultSet resultset = daoColegio.getColegio();

        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        
        Object[] registroLeido = new Object[2];
        while(resultset.next()) {
            registroLeido[0] = resultset.getInt("idColegio");
            registroLeido[1] = resultset.getString("nombre");
                        
            modelo.addRow(registroLeido);
        }
        tablaColegio.setModel(modelo);
         }catch(Exception e){
             e.printStackTrace();
         }
        
    }
