    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed
        int fila = tablaColegio.getSelectedRow();
        int codigoColegio = Integer.parseInt(tablaColegio.getValueAt(fila, 0).toString());
        String nombre = tablaColegio.getValueAt(fila, 1).toString();
        codigoColegioEliminado = codigoColegio;
        nombreColegioEliminado = nombre;
        daoColegio.EliminarColegio(codigoColegio);
        cargarTablaColegio();
    }//GEN-LAST:event_buttonEliminarActionPerformed

