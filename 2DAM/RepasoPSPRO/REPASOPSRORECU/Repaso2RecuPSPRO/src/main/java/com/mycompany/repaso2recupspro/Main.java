    public void cargarTablaLibros(){
        try{
            ResultSet resultset = daolibros.getLibros();

        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Categoria");
        
        Object[] registroLeido = new Object[3];
        while(resultset.next()) {
            registroLeido[0] = resultset.getInt("idLibros");
            registroLeido[1] = resultset.getString("nombre");
            registroLeido[2] = resultset.getString("categoria");
                        
            modelo.addRow(registroLeido);
        }
        libroTable.setModel(modelo);
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }
    
    public void cargarTablaCategoria() {
         try{
             ResultSet resultset = daocategoria.getCategoria();

        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        
        Object[] registroLeido = new Object[2];
        while(resultset.next()) {
            registroLeido[0] = resultset.getInt("id");
            registroLeido[1] = resultset.getString("nombre");
                        
            modelo.addRow(registroLeido);
        }
        CategoriaTable.setModel(modelo);
         }catch(Exception e){
             e.printStackTrace();
         }
        
    }
    
    
    
