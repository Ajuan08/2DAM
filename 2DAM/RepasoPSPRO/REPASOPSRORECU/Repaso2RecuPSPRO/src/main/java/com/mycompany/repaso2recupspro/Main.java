    private void buttonInsertarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInsertarLibroActionPerformed
    String idLibroText = txtIDLibro.getText();
    String nombreLibro = txtNombreLibro.getText();
    int categoria = comboBoxLibro.getSelectedIndex()+1;

        if (!idLibroText.isEmpty() && !nombreLibro.isEmpty()) {
            try {
                int idLibro = Integer.parseInt(idLibroText);
                daolibros.insertarLibro(idLibro, nombreLibro, categoria);
                cargarTablaLibros();
            } catch (NumberFormatException e) {
                System.out.println("Error al convertir a entero: " + e.getMessage());
            }
        } else {
            System.out.println("Los campos ID del libro y Nombre del libro no pueden estar vacíos");
        }
    }//GEN-LAST:event_buttonInsertarLibroActionPerformed

    private void buttonBorrarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBorrarLibroActionPerformed
    int fila = libroTable.getSelectedRow();
        if (fila != -1) {
            int id = Integer.parseInt(libroTable.getValueAt(fila, 0).toString());
            String nombre = libroTable.getValueAt(fila, 1).toString();
            int categoria = Integer.parseInt(libroTable.getValueAt(fila, 0).toString());

            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de que quieres borrar el libro?", "Confirmar Borrado", JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION) {
                daolibros.borrarLibros(id, nombre, categoria);
                cargarTablaLibros();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona un libro para borrar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonBorrarLibroActionPerformed

