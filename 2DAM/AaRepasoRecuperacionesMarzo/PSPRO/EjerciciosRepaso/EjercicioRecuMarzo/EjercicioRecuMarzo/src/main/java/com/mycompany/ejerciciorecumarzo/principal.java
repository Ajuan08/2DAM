    private void botonListadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListadoActionPerformed
        try {
        // Obtener los nombres de la base de datos
        ResultSet resultSet = daopersonas.getPersonas();

        // Crear una lista para almacenar los nombres
        List<String> nombres = new ArrayList<>();

        // Agregar los nombres a la lista
        while (resultSet.next()) {
            String nombre = resultSet.getString("nombre");
            nombres.add(nombre);
        }

        // Ordenar la lista alfabéticamente
        Collections.sort(nombres);

        // Construir el String con los nombres ordenados
        StringBuilder sb = new StringBuilder();
        for (String nombre : nombres) {
            sb.append(nombre).append("\n");
        }

        // Mostrar el String en el TextArea
        lista.setText(sb.toString());
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error al obtener los nombres", "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_botonListadoActionPerformed

    private void tablaPersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPersonasMouseClicked
        int filaSeleccionada = tablaPersonas.getSelectedRow();
        if (filaSeleccionada != -1) {
            int idPersona = Integer.parseInt(tablaPersonas.getValueAt(filaSeleccionada, 0).toString());

            try {
                ResultSet resultSetVivienda = daopersonas.getViviendaPersona(idPersona);

                DefaultTableModel modeloVivienda = new DefaultTableModel();
                modeloVivienda.addColumn("ID");
                modeloVivienda.addColumn("DIRECCIÓN");
                modeloVivienda.addColumn("NUMERO");
                modeloVivienda.addColumn("ZONA PEGRILOSA");
                modeloVivienda.addColumn("ID PERSONA");
                // Añade más columnas según los datos que quieras mostrar de la vivienda

                while (resultSetVivienda.next()) {
                    Object[] fila = new Object[5]; // Ajusta el tamaño según el número de columnas
                    fila[0] = resultSetVivienda.getInt("idvivienda");
                    fila[1] = resultSetVivienda.getString("direccion");
                    fila[2] = resultSetVivienda.getInt("numero");
                    fila[3] = resultSetVivienda.getBoolean("esChungo");
                    fila[4] = resultSetVivienda.getInt("idpersona");
                    // Añade más elementos al arreglo según los datos que quieras mostrar de la vivienda

                    modeloVivienda.addRow(fila);
                }

                tablaVivienda.setModel(modeloVivienda);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error al cargar los datos de la vivienda", "Error", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_tablaPersonasMouseClicked

