    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        int filaSeleccionada = tablaPersonas.getSelectedRow();
        if (filaSeleccionada != -1) {
            int idAnterior = Integer.parseInt(tablaPersonas.getValueAt(filaSeleccionada, 0).toString());
            int nuevoId = Integer.parseInt(textID.getText());
            String nombre = textNombre.getText();

            daopersonas.modificarPersona(idAnterior, nuevoId, nombre);
            cargarTablaPersonas();
            limpiarTexts();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una persona para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonModificarActionPerformed

    private void botonInsertarViviendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarViviendaActionPerformed
         String idvivienda = textIDVivienda.getText();
        try{
            if(idvivienda.isEmpty()) {
                throw new ExcepcionInsertVacio();
            } else{
                int id = Integer.parseInt(idvivienda);
                String direccion = textDireccion.getText();
                int numero = Integer.parseInt(textNumero.getText());
                boolean esChungo = Boolean.parseBoolean(textZonaChunga.getText());
                int idpersona = Integer.parseInt(textIDPersona1.getText());
                daopersonas.insertarVivienda(id, direccion, numero, esChungo,idpersona);
                cargarTablaVivienda();
                limpiarTexts();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());        
        }  
    }//GEN-LAST:event_botonInsertarViviendaActionPerformed

